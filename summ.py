# сложить числа

marks = [5, 5, 4, 5, 3, 3, 2, 5, 4]
marks_sum = 0
for mark in marks:
    marks_sum = marks_sum + mark # тут должно быть что-то с mark и marks_sum

print(marks_sum) # тут должно быть 36

